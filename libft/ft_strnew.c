#include "libft.h"

char	*ft_strnew(size_t size)
{
	char	*str;

	str = (char *)malloc((size + 1) * sizeof(char));
	if (str == NULL)
		return (NULL);
	size++;
	while (size > 0)
	{
		str[size - 1] = '\0';
		size--;
	}
	return (str);
}
