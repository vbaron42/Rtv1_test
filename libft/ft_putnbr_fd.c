#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	char	c;

	if (n == (-2147483647 - 1))
	{
		write(fd, "-2147483648", 11);
		return ;
	}
	else if (0 > n)
	{
		write(fd, "-", 1);
		n = -n;
	}
	if (n >= 10)
	{
		ft_putnbr_fd(n / 10, fd);
	}
	c = (n % 10) + '0';
	write(fd, &c, 1);
}
